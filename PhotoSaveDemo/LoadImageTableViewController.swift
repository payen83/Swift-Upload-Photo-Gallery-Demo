    func loadImageFromPath(path: String) -> UIImage? {
        
        let image = UIImage(contentsOfFile: path)
        
        if image == nil {
            
            print("missing image at: \(path)")
        }
        print("Loading image from path: \(path)") // this is just for you to see the path in case you want to go to the directory, using Finder.
        return image
        
    }

    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("cell", forIndexPath: indexPath) as! LoadImageTableViewCell
        
        let myImage = myImageArray[indexPath.row]
        
        let myPath = myImage.valueForKey("imagePath") as? String
        
        let loadImage = loadImageFromPath(myPath!)

        //cell.textLabel!.text = myGroceriesList.valueForKey("itemName") as? String
        cell.myImageList.image = loadImage
        cell.myText.text = "This is testing"

        // Configure the cell...

        return cell
    }


